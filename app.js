// ====== تحديث المراكز عند تغيير القطاع ======
function تحديث_المراكز(معرف_القطاع, معرف_المركز) {
  var قطاع = document.getElementById(معرف_القطاع);
  var قائمة_مراكز = document.getElementById(معرف_المركز);
  if (!قطاع || !قائمة_مراكز) return;
  var قيمة = قطاع.value;
  قائمة_مراكز.innerHTML = '<option value="">-- اختر المركز --</option>';
  if (!قيمة) return;
  var مراكز = مراكز_القطاع(قيمة);
  مراكز.forEach(function(مركز) {
    قائمة_مراكز.innerHTML += '<option value="' + مركز + '">' + مركز + '</option>';
  });
}

// ====== تحميل البيانات من Google Sheets ======
function تحميل_البيانات() {
  إظهار_تحميل('جاري تحميل البيانات...');
  تحميل_شيت('الأسطول', function(بيانات) {
    if (بيانات && بيانات.length > 0) {
      بيانات_الاسطول_المحلية = بيانات;
      وضع_الاتصال = true;
    } else {
      بيانات_الاسطول_المحلية = قاعدة_بيانات_الاسطول;
      وضع_الاتصال = false;
    }
    تحديث_حالة_الاتصال();
    ملء_قوائم_اللوحات();
    تحديث_لوحة_القائد();
    إخفاء_تحميل();
  });
  تحميل_شيت('رصد المركبات', function(بيانات) {
    if (بيانات) بيانات_الرصد = بيانات;
  });
  تحميل_شيت('الحوادث', function(بيانات) {
    if (بيانات) { بيانات_الحوادث = بيانات; تحديث_لوحة_القائد(); }
  });
  تحميل_شيت('الصيانة الوقائية', function(بيانات) {
    if (بيانات) { بيانات_الصيانة_الوقائية = بيانات; تحديث_لوحة_القائد(); }
  });
  تحميل_شيت('تغيير الزيت', function(بيانات) {
    if (بيانات) { بيانات_تغيير_الزيت = بيانات; تحديث_لوحة_القائد(); فحص_تنبيهات_الزيت(); }
  });
}

// ====== تحميل كل البيانات دفعة واحدة ======
var _بيانات_كاملة_محملة = false;
function تحميل_كل_البيانات_دفعة_واحدة() {
  if (_بيانات_كاملة_محملة) return;
  var اسم_callback = 'cb_all_' + Date.now();
  window[اسم_callback] = function(استجابة) {
    try { delete window[اسم_callback]; } catch(e) {}
    if (استجابة && (استجابة.status === 'success' || استجابة.fleet !== undefined)) {
      _بيانات_كاملة_محملة = true;
      وضع_الاتصال = true;
      // تحميل بيانات الأسطول
      if (استجابة.fleet && استجابة.fleet.length > 0) {
        بيانات_الاسطول_المحلية = استجابة.fleet;
        ملء_قوائم_اللوحات();
      }
      // تحميل بيانات الرصد
      if (استجابة.tracking) بيانات_الرصد = استجابة.tracking;
      // تحميل بيانات الحوادث
      if (استجابة.accidents) {
        بيانات_الحوادث = استجابة.accidents;
        تحديث_جدول_الحوادث();
      }
      // تحميل بيانات الصيانة
      if (استجابة.maintenance) {
        بيانات_الصيانة_الوقائية = استجابة.maintenance;
        تحديث_جدول_الصيانة();
      }
      // تحميل بيانات تغيير الزيت
      if (استجابة.oil) {
        بيانات_تغيير_الزيت = استجابة.oil;
        تحديث_جدول_تغيير_الزيت();
        فحص_تنبيهات_الزيت();
      }
      تحديث_لوحة_القائد();
      تحديث_حالة_الاتصال();
    } else {
      وضع_الاتصال = false;
      تحديث_حالة_الاتصال();
    }
  };
  var script = document.createElement('script');
  script.src = SCRIPT_URL + '?action=getAll&callback=' + اسم_callback;
  script.onerror = function() {
    try { delete window[اسم_callback]; } catch(e) {}
    وضع_الاتصال = false;
    تحديث_حالة_الاتصال();
  };
  document.head.appendChild(script);
  setTimeout(function() {
    if (window[اسم_callback]) {
      try { delete window[اسم_callback]; } catch(e) {}
      وضع_الاتصال = false;
      تحديث_حالة_الاتصال();
    }
  }, 15000);
}

// ====== تحميل شيت واحد (للتوافق) ======
function تحميل_شيت(اسم_الشيت, دالة_الاستجابة) {
  // استخدام getAll بدلاً من get لأن السكريبت المنشور يدعمها
  var اسم_callback = 'cb_' + اسم_الشيت.replace(/\s/g,'_') + '_' + Date.now();
  window[اسم_callback] = function(استجابة) {
    try { delete window[اسم_callback]; } catch(e) {}
    if (استجابة && (استجابة.status === 'success' || استجابة.fleet !== undefined)) {
      // تحديد البيانات المطلوبة حسب اسم الشيت
      var خريطة = {
        'الأسطول': 'fleet',
        'رصد المركبات': 'tracking',
        'الحوادث': 'accidents',
        'الصيانة الوقائية': 'maintenance',
        'تغيير الزيت': 'oil'
      };
      var مفتاح = خريطة[اسم_الشيت];
      دالة_الاستجابة(مفتاح && استجابة[مفتاح] ? استجابة[مفتاح] : []);
    } else {
      دالة_الاستجابة(null);
    }
  };
  var script = document.createElement('script');
  script.src = SCRIPT_URL + '?action=getAll&callback=' + اسم_callback;
  script.onerror = function() {
    try { delete window[اسم_callback]; } catch(e) {}
    دالة_الاستجابة(null);
  };
  document.head.appendChild(script);
  setTimeout(function() {
    if (window[اسم_callback]) {
      try { delete window[اسم_callback]; } catch(e) {}
      دالة_الاستجابة(null);
    }
  }, 15000);
}

// ====== حفظ في شيت ======
function حفظ_في_شيت(اسم_الشيت, بيانات_الصف, دالة_الاستجابة) {
  var اسم_callback = 'save_' + Date.now();
  window[اسم_callback] = function(استجابة) {
    try { delete window[اسم_callback]; } catch(e) {}
    if (دالة_الاستجابة) دالة_الاستجابة(استجابة);
  };
  var script = document.createElement('script');
  script.src = SCRIPT_URL + '?action=add' +
    '&sheet=' + encodeURIComponent(اسم_الشيت) +
    '&data=' + encodeURIComponent(JSON.stringify(بيانات_الصف)) +
    '&callback=' + اسم_callback;
  script.onerror = function() {
    try { delete window[اسم_callback]; } catch(e) {}
    if (دالة_الاستجابة) دالة_الاستجابة({status: 'error'});
  };
  document.head.appendChild(script);
}

