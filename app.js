// ====== تهيئة قوائم السنوات ======
function تهيئة_قوائم_السنوات() {
  var السنة_الحالية = new Date().getFullYear();
  var قوائم = document.querySelectorAll('select[data-نوع="سنة"]');
  قوائم.forEach(function(قائمة) {
    قائمة.innerHTML = '';
    for (var س = السنة_الحالية - 1; س <= السنة_الحالية + 1; س++) {
      var خيار = document.createElement('option');
      خيار.value = س;
      خيار.textContent = س;
      if (س === السنة_الحالية) خيار.selected = true;
      قائمة.appendChild(خيار);
    }
  });
}

// ====== ضبط التواريخ الافتراضية ======
function ضبط_التواريخ_الافتراضية() {
  var اليوم = new Date().toISOString().split('T')[0];
  var الوقت = new Date().toTimeString().slice(0, 5);
  document.querySelectorAll('input[type="date"]').forEach(function(حقل) {
    if (!حقل.value) حقل.value = اليوم;
  });
  document.querySelectorAll('input[type="time"]').forEach(function(حقل) {
    if (!حقل.value) حقل.value = الوقت;
  });
}

// ====== ملء قوائم النماذج ======
function ملء_قوائم_النماذج() {
  // أنواع الأعطال - نموذج الصيانة
  var قائمة_نوع_عطل = document.getElementById('صيانة_نوع_عطل');
  if (قائمة_نوع_عطل) {
    قائمة_نوع_عطل.innerHTML = '<option value="">-- اختر --</option>';
    بيانات_الصيانة.انواع_الاعطال.forEach(function(نوع) {
      قائمة_نوع_عطل.innerHTML += '<option value="' + نوع + '">' + نوع + '</option>';
    });
  }
  // أوصاف الأعطال - نموذج الصيانة
  var قائمة_وصف_عطل = document.getElementById('صيانة_وصف_عطل');
  if (قائمة_وصف_عطل) {
    قائمة_وصف_عطل.innerHTML = '<option value="">-- اختر --</option>';
    بيانات_الصيانة.اوصاف_الاعطال.forEach(function(وصف) {
      قائمة_وصف_عطل.innerHTML += '<option value="' + وصف + '">' + وصف + '</option>';
    });
  }
  // الفنيون - جميع النماذج
  var قائمة_فنيون = document.querySelectorAll('select[data-نوع="فني"]');
  قائمة_فنيون.forEach(function(قائمة) {
    قائمة.innerHTML = '<option value="">-- اختر الفني --</option>';
    بيانات_الصيانة.الفنيون.forEach(function(فني) {
      قائمة.innerHTML += '<option value="' + فني + '">' + فني + '</option>';
    });
  });
  // قطع الغيار - نموذج الصيانة
  var قائمة_قطع = document.getElementById('صيانة_قطع_غيار');
  if (قائمة_قطع) {
    قائمة_قطع.innerHTML = '<option value="">-- اختر --</option>';
    بيانات_الصيانة.قطع_الغيار.forEach(function(قطعة) {
      قائمة_قطع.innerHTML += '<option value="' + قطعة + '">' + قطعة + '</option>';
    });
  }
  // ملء قوائم القطاعات
  ملء_قوائم_القطاعات();
  // ملء قوائم الفلتر في صفحة الأسطول
  ملء_قوائم_فلتر_الاسطول();
}

