function تحديث_جدول_الاسطول(قطاع_محدد, حالة_محددة, نص_بحث) {
  var جدول = document.getElementById('جدول_الاسطول');
  if (!جدول) return;
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var مفلترة = مصدر.filter(function(م) {
    var قطاع_م = م['القطاع'] || (Array.isArray(م) ? م[4] : '') || '';
    var حالة_م = م['الحالة'] || (Array.isArray(م) ? م[6] : '') || '';
    var رقم_م = م['رقم_اللوحة'] || (Array.isArray(م) ? م[0] : '') || '';
    var نوع_م = م['النوع'] || (Array.isArray(م) ? م[1] : '') || '';
    if (قطاع_محدد && قطاع_م !== قطاع_محدد) return false;
    if (حالة_محددة && حالة_م !== حالة_محددة) return false;
    if (نص_بحث && رقم_م.toLowerCase().indexOf(نص_بحث) === -1 && نوع_م.toLowerCase().indexOf(نص_بحث) === -1) return false;
    return true;
  });

  var tbody = جدول.querySelector('tbody');
  if (!tbody) return;
  tbody.innerHTML = '';
  مفلترة.forEach(function(م, i) {
    var حالة = م['الحالة'] || (Array.isArray(م) ? م[6] : '') || 'عاملة';
    var صف_لون = حالة === 'عاملة' ? '' : (حالة === 'صيانة' ? 'صف-تحذير' : 'صف-خطر');
    var صف = '<tr class="' + صف_لون + '">' +
      '<td>' + (i + 1) + '</td>' +
      '<td><strong>' + (م['رقم_اللوحة'] || (Array.isArray(م) ? م[0] : '') || '') + '</strong></td>' +
      '<td>' + (م['النوع'] || (Array.isArray(م) ? م[1] : '') || '') + '</td>' +
      '<td>' + (م['الموديل'] || (Array.isArray(م) ? م[2] : '') || '') + '</td>' +
      '<td dir="ltr">' + (م['رقم_الشاصي'] || (Array.isArray(م) ? م[3] : '') || '') + '</td>' +
      '<td>' + (م['القطاع'] || (Array.isArray(م) ? م[4] : '') || '') + '</td>' +
      '<td>' + (م['المركز'] || (Array.isArray(م) ? م[5] : '') || '') + '</td>' +
      '<td><span class="شارة ' + حالة + '">' + حالة + '</span></td>' +
      '<td>' + ((parseInt(م['آخر_قراءة_عداد'] || (Array.isArray(م) ? م[7] : 0)) || 0)).toLocaleString('ar-SA') + ' كم</td>' +
      '</tr>';
    tbody.innerHTML += صف;
  });

  var عداد = document.getElementById('عداد_الاسطول');
  if (عداد) عداد.textContent = 'إجمالي: ' + مفلترة.length + ' مركبة من أصل ' + مصدر.length;
}

