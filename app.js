// ====== تحديث مؤشر الزيت عند اختيار مركبة ======
function تحديث_مؤشر_الزيت(مركبة) {
  var مؤشر = document.getElementById('مؤشر_الزيت');
  if (!مؤشر) return;
  var تاريخ = مركبة['تاريخ_آخر_تغيير_زيت'];
  var قراءة_آخر = parseInt(مركبة['قراءة_عداد_آخر_تغيير_زيت']) || 0;
  var قراءة_حالية = parseInt(مركبة['آخر_قراءة_عداد']) || 0;
  if (!تاريخ) { مؤشر.style.display = 'none'; return; }
  مؤشر.style.display = 'block';
  var فرق_ايام = Math.floor((new Date() - new Date(تاريخ)) / 86400000);
  var فرق_كم = قراءة_حالية - قراءة_آخر;
  var نسبة_كم = Math.min(100, Math.round((فرق_كم / 5000) * 100));
  var نسبة_ايام = Math.min(100, Math.round((فرق_ايام / 30) * 100));
  var نسبة = Math.max(نسبة_كم, نسبة_ايام);
  var شريط = document.getElementById('شريط_الزيت');
  var نص = document.getElementById('نص_مؤشر_الزيت');
  var نسبة_el = document.getElementById('نسبة_مؤشر_الزيت');
  if (شريط) {
    شريط.style.width = نسبة + '%';
    شريط.className = 'شريط-تقدم-داخلي ' + (نسبة >= 100 ? 'خطر' : نسبة >= 80 ? 'تحذير' : 'آمن');
  }
  if (نص) نص.textContent = 'حالة الزيت: ' + فرق_كم.toLocaleString('ar-SA') + ' كم | ' + فرق_ايام + ' يوم';
  if (نسبة_el) نسبة_el.textContent = نسبة + '%';
}
