function توليد_احصائيات_القطاعات(مصدر) {
  var قطاعات = {};
  مصدر.forEach(function(م) {
    var قطاع = م['القطاع'] || (Array.isArray(م) ? م[4] : '') || 'غير محدد';
    var حالة = م['الحالة'] || (Array.isArray(م) ? م[6] : '') || 'عاملة';
    if (!قطاعات[قطاع]) قطاعات[قطاع] = {اجمالي: 0, عاملة: 0, صيانة: 0};
    قطاعات[قطاع].اجمالي++;
    if (حالة === 'عاملة') قطاعات[قطاع].عاملة++;
    else if (حالة === 'صيانة') قطاعات[قطاع].صيانة++;
  });
  var صفوف = '';
  var اجمالي_كل = مصدر.length;
  for (var قطاع in قطاعات) {
    var نسبة = ((قطاعات[قطاع].اجمالي / اجمالي_كل) * 100).toFixed(1);
    صفوف += '<tr><td>' + قطاع + '</td><td>' + قطاعات[قطاع].اجمالي + '</td><td>' + نسبة + '%</td><td>' + قطاعات[قطاع].عاملة + '</td><td>' + قطاعات[قطاع].صيانة + '</td></tr>';
  }
  return صفوف;
}

