function تحديث_رسم_الزيت_الاحصائي() {
  var رسم = الرسوم_البيانية['زيت'];
  if (!رسم) return;
  var ضمن = 0, قريب = 0, تجاوز = 0;
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var اليوم = new Date();
  مصدر.forEach(function(م) {
    var تاريخ = م['تاريخ_آخر_تغيير_زيت'];
    var قراءة_آخر = parseInt(م['قراءة_عداد_آخر_تغيير_زيت']) || 0;
    var قراءة_حالية = parseInt(م['آخر_قراءة_عداد'] || (Array.isArray(م) ? م[7] : 0)) || 0;
    if (تاريخ) {
      var فرق_ايام = (اليوم - new Date(تاريخ)) / 86400000;
      var فرق_كم = قراءة_حالية - قراءة_آخر;
      if (فرق_كم >= 5000 || فرق_ايام >= 30) تجاوز++;
      else if (فرق_كم >= 4000 || فرق_ايام >= 25) قريب++;
      else ضمن++;
    } else ضمن++;
  });
  رسم.data.datasets[0].data = [ضمن, قريب, تجاوز];
  رسم.update();
}

