// ====== ملء قوائم اللوحات ======
function ملء_قوائم_اللوحات() {
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var قوائم = document.querySelectorAll('select[data-نوع="لوحة"]');
  قوائم.forEach(function(قائمة) {
    قائمة.innerHTML = '<option value="">-- اختر رقم اللوحة --</option>';
    مصدر.forEach(function(م) {
      var رقم = م['رقم_اللوحة'] || (Array.isArray(م) ? م[0] : '');
      var نوع = م['النوع'] || (Array.isArray(م) ? م[1] : '');
      if (رقم) {
        قائمة.innerHTML += '<option value="' + رقم + '">' + رقم + (نوع ? ' - ' + نوع : '') + '</option>';
      }
    });
  });
}

// ====== سحب بيانات المركبة عند اختيار اللوحة ======
function عند_اختيار_لوحة(رقم_اللوحة, بادئة) {
  if (!رقم_اللوحة) return;
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var مركبة = null;
  for (var i = 0; i < مصدر.length; i++) {
    var رقم = مصدر[i]['رقم_اللوحة'] || (Array.isArray(مصدر[i]) ? مصدر[i][0] : '');
    if (رقم === رقم_اللوحة) { مركبة = مصدر[i]; break; }
  }
  if (!مركبة) مركبة = ابحث_عن_مركبة(رقم_اللوحة);
  if (!مركبة) return;

  var حقول = {
    '_نوع': مركبة['النوع'] || (Array.isArray(مركبة) ? مركبة[1] : ''),
    '_موديل': مركبة['الموديل'] || (Array.isArray(مركبة) ? مركبة[2] : ''),
    '_شاصي': مركبة['رقم_الشاصي'] || (Array.isArray(مركبة) ? مركبة[3] : ''),
    '_قطاع': مركبة['القطاع'] || (Array.isArray(مركبة) ? مركبة[4] : ''),
    '_مركز': مركبة['المركز'] || (Array.isArray(مركبة) ? مركبة[5] : ''),
    '_عداد': مركبة['آخر_قراءة_عداد'] || (Array.isArray(مركبة) ? مركبة[7] : '')
  };

  for (var حقل in حقول) {
    var عنصر = document.getElementById(بادئة + حقل);
    if (عنصر) {
      if (عنصر.tagName === 'SELECT') {
        عنصر.value = حقول[حقل];
        if (حقل === '_قطاع') {
          تحديث_المراكز(بادئة + '_قطاع', بادئة + '_مركز');
          setTimeout(function() {
            var م_مركز = document.getElementById(بادئة + '_مركز');
            if (م_مركز) م_مركز.value = حقول['_مركز'];
          }, 100);
        }
      } else {
        عنصر.value = حقول[حقل];
      }
    }
  }

  // عرض بطاقة معلومات المركبة
  var بطاقة = document.getElementById(بادئة + '_بطاقة_المركبة');
  if (بطاقة) {
    بطاقة.style.display = 'grid';
    // عرض بيانات المركبة في عناصر البطاقة
    var عنصر_نوع = document.getElementById(بادئة + '_نوع_عرض');
    var عنصر_موديل = document.getElementById(بادئة + '_موديل_عرض');
    var عنصر_شاصي = document.getElementById(بادئة + '_شاصي_عرض');
    var عنصر_حالة = document.getElementById(بادئة + '_حالة_عرض');
    if (عنصر_نوع) عنصر_نوع.textContent = حقول['النوع'];
    if (عنصر_موديل) عنصر_موديل.textContent = حقول['الموديل'];
    if (عنصر_شاصي) عنصر_شاصي.textContent = حقول['الشاصي'];
    if (عنصر_حالة) عنصر_حالة.textContent = مركبة['الحالة'] || (Array.isArray(مركبة) ? مركبة[6] : 'عاملة');
    // عرض بيانات خاصة بتغيير الزيت
    if (بادئة === 'زيت') {
      var عنصر_آخر_تغيير = document.getElementById('زيت_آخر_تغيير');
      var عنصر_آخر_قراءة = document.getElementById('زيت_آخر_قراءة');
      if (عنصر_آخر_تغيير) عنصر_آخر_تغيير.textContent = مركبة['تاريخ_آخر_تغيير_زيت'] || '-';
      if (عنصر_آخر_قراءة) عنصر_آخر_قراءة.textContent = (مركبة['آخر_قراءة_عداد'] || 0).toLocaleString('ar-SA') + ' كم';
      // تحديث مؤشر الزيت
      تحديث_مؤشر_الزيت(مركبة);
    }
  }
}

// ====== تحديث لوحة القائد ======
function تحديث_لوحة_القائد() {
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var عاملة = 0, صيانة = 0, خارج = 0;
  مصدر.forEach(function(م) {
    var حالة = م['الحالة'] || (Array.isArray(م) ? م[6] : '') || 'عاملة';
    if (حالة === 'عاملة') عاملة++;
    else if (حالة === 'صيانة') صيانة++;
    else خارج++;
  });

  var حوادث_مفتوحة = بيانات_الحوادث.filter(function(ح) {
    return (ح['الحالة'] || '') !== 'مغلق';
  }).length;
  var صيانة_معلقة = بيانات_الصيانة_الوقائية.filter(function(ص) {
    return (ص['حالة_الإنجاز'] || '') !== 'منجز';
  }).length;
  var تنبيهات = حساب_تنبيهات_الزيت();

  تحديث_عنصر('إجمالي_الأسطول', مصدر.length);
  تحديث_عنصر('مركبات_جاهزة', عاملة);
  تحديث_عنصر('تحت_الصيانة', صيانة);
  تحديث_عنصر('خارج_الخدمة', خارج);
  تحديث_عنصر('حوادث_مسجلة', حوادث_مفتوحة);
  تحديث_عنصر('طلبات_معلقة', صيانة_معلقة);
  تحديث_عنصر('تنبيهات_الزيت', تنبيهات);

  تحديث_رسم_حالة_الاسطول(عاملة, صيانة, خارج);
  تحديث_رسم_الصيانة_الشهري();
  تحديث_رسم_الحوادث_الشهري();
  تحديث_رسم_الزيت_الاحصائي();
}

