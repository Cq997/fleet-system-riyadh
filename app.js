function فحص_تنبيهات_الزيت() {
  var حاوية = document.getElementById('قائمة_تنبيهات_الزيت');
  if (!حاوية) return;
  var مصدر = بيانات_الاسطول_المحلية.length > 0 ? بيانات_الاسطول_المحلية : قاعدة_بيانات_الاسطول;
  var اليوم = new Date();
  var تنبيهات = [];
  مصدر.forEach(function(م) {
    var تاريخ = م['تاريخ_آخر_تغيير_زيت'];
    var قراءة_آخر = parseInt(م['قراءة_عداد_آخر_تغيير_زيت']) || 0;
    var قراءة_حالية = parseInt(م['آخر_قراءة_عداد'] || (Array.isArray(م) ? م[7] : 0)) || 0;
    var رقم = م['رقم_اللوحة'] || (Array.isArray(م) ? م[0] : '');
    if (تاريخ) {
      var فرق_ايام = Math.floor((اليوم - new Date(تاريخ)) / 86400000);
      var فرق_كم = قراءة_حالية - قراءة_آخر;
      if (فرق_كم >= 4500 || فرق_ايام >= 25) {
        تنبيهات.push({ رقم: رقم, فرق_كم: فرق_كم, فرق_ايام: فرق_ايام, خطر: فرق_كم >= 5000 || فرق_ايام >= 30 });
      }
    }
  });

  if (تنبيهات.length === 0) {
    حاوية.innerHTML = '<div class="لا-تنبيهات">لا توجد تنبيهات حالياً ✅</div>';
    return;
  }

  حاوية.innerHTML = '';
  تنبيهات.slice(0, 15).forEach(function(ت) {
    var div = document.createElement('div');
    div.className = 'عنصر-تنبيه ' + (ت.خطر ? 'تنبيه-خطر' : 'تنبيه-تحذير');
    div.innerHTML = '<span class="رقم-لوحة-تنبيه">' + ت.رقم + '</span>' +
      '<span class="تفاصيل-تنبيه">' + ت.فرق_كم.toLocaleString('ar-SA') + ' كم | ' + ت.فرق_ايام + ' يوم</span>' +
      '<span class="نوع-تنبيه">' + (ت.خطر ? '⚠️ تجاوز الموعد' : '🔔 قريب من الموعد') + '</span>';
    حاوية.appendChild(div);
  });
}

